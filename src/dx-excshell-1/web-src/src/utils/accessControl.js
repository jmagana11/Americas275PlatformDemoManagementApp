/**
 * Check if a user has access to User Management
 * @param {Object} ims - The IMS object containing user profile information
 * @returns {boolean} - True if user has access, false otherwise
 */
export const hasUserManagementAccess = (ims) => {
  if (!ims?.profile?.email) {
    console.warn('No email found in IMS profile, denying access to User Management')
    return false
  }
  
  const userEmail = ims.profile.email.toLowerCase()
  
  // Check if user is in the allowed list
  if (ALLOWED_USER_MANAGEMENT_EMAILS.includes(userEmail)) {
    console.log(`User ${userEmail} is allowed to access User Management`)
    return true
  }
  
  console.log(`User ${userEmail} is not in the allowed list for User Management`)
  return false
}

